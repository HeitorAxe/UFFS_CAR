    private final List<List<String>> listaClientes= db.selectAllClientes();
    
    private boolean existsCPF(String cpf) throws Exception{
        for(int i = 0; i < this.listaClientes.size(); i++)
        {
            if(listaClientes.get(i).get(3).equals(cpf))
            {
                
                return true;
            }
        }
        return false;
    }
    
    private void DelClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DelClienteActionPerformed
        try {
            if(!existsCPF(cpf.getText()))
            {
                JOptionPane.showMessageDialog(null, "CPF InvÃ¡lido");
            }
        } catch (Exception ex) {
            Logger.getLogger(DeletarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        Update app = new Update();
        try {
            app.deleteFromClients(cpf.getText());
        } catch (SQLException ex) {
            Logger.getLogger(DeletarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        super.dispose();
    }//GEN-LAST:event_DelClienteActionPerformed

